package com.ericsson.derby;

/**
 *
 * @author EKARPIA_local
 */
public class CodeGenerationException extends Exception {


	/**
	 * Creates a new instance of
	 * <code>CodeGenerationException</code> without detail message.
	 */
	public CodeGenerationException() {
	}

	/**
	 * Constructs an instance of
	 * <code>CodeGenerationException</code> with the specified detail message.
	 *
	 * @param msg the detail message.
	 */
	public CodeGenerationException(String msg) {
		super(msg);
	}
}
